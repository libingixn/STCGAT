def masked_mae(preds, labels, null_val=np.nan):
    if np.isnan(null_val):
        mask = ~torch.isnan(labels)
    else:
        mask = (labels != null_val)
    mask = mask.float()
    mask /= torch.mean((mask))
    mask = torch.where(torch.isnan(mask), torch.zeros_like(mask), mask)
    loss = torch.abs(preds - labels)
    loss = loss * mask
    loss = torch.where(torch.isnan(loss), torch.zeros_like(loss), loss)
    return torch.mean(loss)

def r2(pred, y):
    """
    :param y: ground truth
    :param pred: predictions
    :return: R square (coefficient of determination)
    """
    return 1 - np.sum((y - pred) ** 2) / np.sum((y - np.mean(pred)) ** 2)

def smape(pred, y):
    return 2.0 * torch.mean(torch.abs(pred - y) / (torch.abs(pred) + torch.abs(y))) * 100

def explained_variance(pred, y):
    return 1 - torch.var(y - pred) / torch.var(y)
